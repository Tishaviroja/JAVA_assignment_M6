package com.collection;

public class student 
{
	private int sid;
	private String sname, city;
	public int getSid()
	{
		return sid;
	}
	public void setSid(int sid)
	{
		this.sid=sid;
	}
	public String getsname()
	{
		return sname;
	}
	public void setname(String sname)
	{
		this.sname=sname;
	}
	public String getCity()
	{
		return city;
	}
	public void setCity(String city)
	{
		this.city=city;
	}
}
