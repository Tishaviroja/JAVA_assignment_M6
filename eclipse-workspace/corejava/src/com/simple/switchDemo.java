package com.simple;

public class switchDemo 
{
	public static void main(String[] args)
	{
		int no=1;
		switch(no)
		{
		case 1:
			System.out.println("your no is one....."+no);
			break;
		case 2:
			System.out.println("your no is two....."+no);
			break;
		case 3:
			System.out.println("your no is three....."+no);
			break;
			default:
				
				System.out.println("invalid no...");
				break;
		}
	}
	
}
